public class Produto {
    private int codigo;
    private String descricao;
    private Double preco;
    private int quantidade;

    Produto(int codigo, String descricao, double preco, int quantidade){
        this.codigo = codigo;
        this.descricao = descricao;
        this.preco = preco;
        this.quantidade = quantidade; 
    }

    


}
